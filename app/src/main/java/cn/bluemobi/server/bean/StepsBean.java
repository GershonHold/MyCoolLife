package cn.bluemobi.server.bean;

public class StepsBean {


    private int si_id;
    private String date;
    private int steps;
    private int usr_id;

    public int getSi_id() {
        return si_id;
    }

    public void setSi_id(int si_id) {
        this.si_id = si_id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getSteps() {
        return steps;
    }

    public void setSteps(int steps) {
        this.steps = steps;
    }

    public int getUsr_id() {
        return usr_id;
    }

    public void setUsr_id(int usr_id) {
        this.usr_id = usr_id;
    }
}
